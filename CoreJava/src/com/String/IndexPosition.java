package com.String;

public class IndexPosition {
	public static void main(String[] args) {
		String a="java Technology";
		System.out.println("Original string = "+a);
		System.out.println();
		
		int index1 = a.charAt(0);
		int index2= a.charAt(13);
		
		System.out.println("The character at position 0 is = "+(char)index1);
		System.out.println();
		
		System.out.println("The character at position 13 is = "+(char)index2);
		System.out.println();
	}

}
