package com.operators;

public class UnaryOperators {
	public static void main(String[] args) {
		
		int i=20;
		//INCREMENT
		System.out.println("increment");
		
		System.out.println(i);
		System.out.println(++i);
		System.out.println(i++);
		System.out.println(i);
		
		//DECREMENT
		int a=10;
		System.out.println("decrement");
		System.out.println(a);
		System.out.println(--a);
		System.out.println(a--);
		System.out.println(a);
	}

}
