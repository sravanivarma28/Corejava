 package com.operators;

	public class LogicalOperators {
		public static void main(String args[]) {
			
			//CONDITIONAL OR LOGICAL OPERATORS
		int a=6;
		int b=7;
		System.out.println(a<b&&b>a);
		System.out.println(a<=b&&b>=a);
		System.out.println(a>b&&b>=+a);
		System.out.println(a==b&&b==a);
		System.out.println(!(a<b&&b>+a));
		System.out.println(!(a==b&&b>+a));
		
		
		}
}
