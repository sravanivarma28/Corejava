package com.gettersandsetters;

public class StudentClass {
private int rank;
private String name;
private int regno;

public int getRank() {
	return rank;
}
public void setRank(int rank) {
	this.rank = rank;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getRegno() {
	return regno;
}
public void setRegno(int regno) {
	this.regno = regno;
}
}
