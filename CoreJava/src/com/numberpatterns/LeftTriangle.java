package com.numberpatterns;

public class LeftTriangle {
	public static void main(String[] args) {
		for(int a=1;a<=4;a++) {
			for(int b=4;b>=a;b--) 
				System.out.print(" ");
				for(int c=1;c<=a;c++) 
			System.out.print(a);
				//System.out.print(c);

				System.out.println();
	}
	}
}
