package com.arrays;

public class ThreeDimension {
	
	public static void main(String[] args) {
			int [][][]arr = {{{1,6,7},{5,6}},{{5,7,8},{3,6},{7,8,9}}};
			
			for(int values[][]:arr) {
				
			for(int num[]:values) {
				
				for(int c:num) {
					System.out.print(c+",");
		
		}
	}}}}
