package com.pojo;

public class Employee {
	private int id;
	public String name;
	protected double slary;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getSlary() {
		return slary;
	}
	public void setSlary(double slary) {
		this.slary = slary;
	}
	

}
