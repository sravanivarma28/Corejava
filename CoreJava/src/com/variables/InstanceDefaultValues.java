package com.variables;

	public class InstanceDefaultValues {
		byte one;
		short two;
		int three;
		long four;
		float five;
		double six;
		String seven;
		char eight;
		boolean nine;
		
		public static void main(String[] args) {
			InstanceDefaultValues idv=new InstanceDefaultValues();
			
			System.out.println(idv.one);
			System.out.println(idv.two);
			System.out.println(idv.three);
			System.out.println(idv.four);
			System.out.println(idv.five);
			System.out.println(idv.six);
			System.out.println(idv.seven);
			System.out.println(idv.eight);
			System.out.println(idv.nine);
			
		}
		

	}
