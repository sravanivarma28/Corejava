package com.variables;
	public class StaticDefaultValues {
		
		static byte one;
		static short two;
		static int three;
		static long four;
		static float five;
		static double six;
		static String seven;
		static char eight;
		static boolean nine;
		
		public static void main(String[] args) {
		//using static varible by using classname
			
			System.out.println(one);
			System.out.println(two);
			System.out.println(StaticDefaultValues.three);
			System.out.println(four);
			System.out.println(five);
			System.out.println(StaticDefaultValues.six);
			System.out.println(seven);
			System.out.println(eight);
			System.out.println(StaticDefaultValues.nine);
		}}
